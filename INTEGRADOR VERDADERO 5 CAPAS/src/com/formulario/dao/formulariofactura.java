    public void CargarTable(){
        
        String []registro = new String[4];
        int cantidad = Integer.parseInt(txtcantidad.getText());
        double precio = Double.parseDouble(txtprecio.getText());
        double valor = cantidad* precio;
        registro[0] = cbdescripcion.getSelectedItem().toString();
        registro[1] = txtprecio.getText();
        registro[2] = txtcantidad.getText();
        registro[3] = Double.toString(valor);
        modelo.addRow(registro);
        Ttabla.setModel(modelo);

        Calcular();
        txtcantidad.setText("");

    }
    public int Calcular(){
        Double valor=0.0d;
        for(int c=0; c<Ttabla.getRowCount();c++){
            valor= valor+ Double.parseDouble(Ttabla.getValueAt(c, 3).toString());
        }
        Double iva = Math.rint(valor*0.14*10)/10;
       
        txttotal.setText(Double.toString(valor));
        txtiva.setText(Double.toString(iva));
        txttotalpagar.setText(Double.toString(valor+iva));   
        return x++;
    }
    
    
    public void Stock(){
       
       int fila=0;
       while(fila<x){
           try{
                String sql= "update productos set cantidad=cantidad-? where descripcion='"+
                        Ttabla.getValueAt(fila,0).toString()+"'";
                
                PreparedStatement ps= conn.prepareStatement(sql);
                ps.setInt(1, Integer.parseInt(Ttabla.getValueAt(fila,2).toString()));
                ps.execute();   
            }catch(Exception ex){
               System.out.println(ex.getMessage());
            }
                   
           fila++;
       }  
    }
    public void GuardarFactura(){
        String sql="insert into factura(fecha, cajero,cliente,valor_cancelado) values(?,?,?,?)";
        try{
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setDate(1, Convertirfecha());
            ps.setString(2, vendedor.getText());
            ps.setString(3, txtcliente.getText());
            ps.setDouble(4, Double.parseDouble(txttotalpagar.getText()));
            ps.executeUpdate();
            
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
        
    }
    public void DetalleFac(){
        String sql="insert into detallefactura(descripcion,precio,cantidad,total,factura) values(?,?,?,?,?)";
        
        int fila=0;
        while(fila<=x){
            try{
                 
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1,Ttabla.getValueAt(fila,0).toString() );
                ps.setDouble(2, Double.parseDouble(Ttabla.getValueAt(fila,1).toString()));
                ps.setInt(3, Integer.parseInt(Ttabla.getValueAt(fila,2).toString()));
                ps.setDouble(4, Double.parseDouble(Ttabla.getValueAt(fila,3).toString()));
                ps.setInt(5, Integer.parseInt(codfactura.getText()));
                ps.executeUpdate();
            }catch(Exception ex){
                
            }
            fila++;
        } 
    }
    
    public void Imprimir(){
        PrinterJob printerJob = PrinterJob.getPrinterJob();
        printerJob.setPrintable(this);
        if(printerJob.printDialog()){
            try{
                printerJob.print();
            }catch(PrinterException ex){
                JOptionPane.showMessageDialog(null, "Error!", "Error\n"+ ex,JOptionPane.ERROR_MESSAGE);
            }
        }
    }
