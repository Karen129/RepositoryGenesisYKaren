    public void CodigoFactura(){
        String sql="select id_factura from factura order by id_factura asc";
        try{
            conn=ConexionBD.Open();
            PreparedStatement ps= conn.prepareStatement(sql);
            ResultSet rs= ps.executeQuery();
            codfactura.setText("1");
            while(rs.next()){
                codfactura.setText(Integer.toString(rs.getInt(1)+1));  
            }
        }catch(Exception ex){
            
        } 
    }
